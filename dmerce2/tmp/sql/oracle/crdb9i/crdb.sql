SPOOL $DMERCE_HOME/log/crdb$DMERCE_ORACLE_VER/$DMERCE_ORACLE_INST/crdb.log
STARTUP NOMOUNT PFILE="$ORACLE_HOME/admin/$DMERCE_ORACLE_INST/pfile/init$DMERCE_ORACLE_INST.ora";
CREATE DATABASE $DMERCE_ORACLE_INST
       MAXINSTANCES 1
       MAXLOGHISTORY 1
       MAXLOGFILES 5
       MAXLOGMEMBERS 5
       MAXDATAFILES 100
       DATAFILE '$DMERCE_ORACLE_DATAFILES/$DMERCE_ORACLE_INST/system01.dbf' SIZE 325M REUSE AUTOEXTEND ON NEXT  10240K MAXSIZE UNLIMITED
       UNDO TABLESPACE "UNDOTBS" DATAFILE '$DMERCE_ORACLE_DATAFILES/$DMERCE_ORACLE_INST/undo01.dbf' SIZE 300M REUSE AUTOEXTEND ON NEXT  5120K MAXSIZE UNLIMITED
       CHARACTER SET WE8ISO8859P15
       NATIONAL CHARACTER SET AL16UTF16
       LOGFILE GROUP 1 ('$DMERCE_ORACLE_DATAFILES/$DMERCE_ORACLE_INST/redo01.log') SIZE 50M,
               GROUP 2 ('$DMERCE_ORACLE_DATAFILES/$DMERCE_ORACLE_INST/redo02.log') SIZE 50M;
